states = [
{
    "name": "One",
    "capital": "1",
    "correct": 0,
    "wrong": 0
}, {
    "name": "Two",
    "capital": "2",
    "correct": 0,
    "wrong": 0
}, {
    "name": "Three",
    "capital": "3",
    "correct": 0,
    "wrong": 0
}]
from ast import If
import random
playing = True
round = 0
correct_this_round = 0
wrong_this_round = 0
random.shuffle(states)

while playing == True:
        
    for state in states:
        capital = input(f"What is the capital associated {state['name']}? ")
        if capital == state['capital']:
            state['correct'] = state['correct'] + 1
            correct_this_round += 1
        else:
            state['wrong'] = state['wrong'] + 1
            wrong_this_round += 1

    print(f"You got {correct_this_round} of them correct!")
    print(f"You got {wrong_this_round} of them wrong!")
    again = input('Do you want to play again? Maybe you can do better. (n/no)')
    if again == 'n' or 'no':
        playing = False
